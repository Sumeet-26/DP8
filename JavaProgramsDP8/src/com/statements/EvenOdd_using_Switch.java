package com.statements;

import java.util.Scanner;

public class EvenOdd_using_Switch {

	public static void main(String[] args) {
		
		Scanner sc=new Scanner (System.in);
		System.out.println("Enter number");
		int num=sc.nextInt();
		
		switch(num%2)
		{
		case 1:System.out.println("This is Even Number");
		break;
		case 2:System.out.println("This is Odd Number");
		break;
		
		
		
		
		
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
