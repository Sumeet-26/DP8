package com.loops;

public class DoWhileDemo {

	public static void main(String[] args) {
		
		int i=1;  //do while loop executes at least once even condition gets false.
		do 
		{
			System.out.println(i);
				i++;
		}
		while(i>=10);
			
		// print 1 to 10 
		int j=1;
		do
		{
			System.out.println(j);
			j++;
		}
		while(j<=10);
		
		
		
		
		
	}

}
