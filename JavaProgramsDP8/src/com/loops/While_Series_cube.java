package com.loops;

public class While_Series_cube {

	public static void main(String[] args) {
		
		//write a code to display series to 10th terms
		//1,8,27,64,125...........
		int i=1;
		int cube;
		
		while(i<=10)
		{
			cube=i*i*i;
			i++;
			System.out.println(cube);
		}
		
		
		
		
		
		
		
		
		
		
		
		

	}

}
