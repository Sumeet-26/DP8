package com.loops;

public class WhileTerms {

	public static void main(String[] args) {
		
		//write a code to display series to 10th terms
		//10,20,30,40,50,60.......
		
		int i=1;
		int sum;
		while(i<=10)
		{
			
			sum=i*10;
			i++;
			System.out.println(sum);
		}
	
		
		
		
		
		
		
		
	}

}
