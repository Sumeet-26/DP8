package com.loops;

public class Odd_No_Count {

	public static void main(String[] args) {
		
		//write code to count number of odd numbers between 1 to 10
		int i;
		int count=0;
		for (i=1; i<=10; i++)
		{
			if(i%2!=0) 
			{
				count=count+i;
			}
			System.out.println(count);
		}
		
		
		
		
		
		
		
		
		
		
	}

}
