package com.basic;

public class Cm_to_m_km {

	public static void main(String[] args) 
	{

		// 100cm=1meter

		double cm = 50.345;
		double meter = cm / 100;
		System.out.println("Cm to meter is " + meter);

		// 1km=1000m
		double km = meter / 1000;
		System.out.println("Meter to km is " + km);

	}

}
